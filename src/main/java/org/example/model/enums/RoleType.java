package org.example.model.enums;

/**
 * Enum reprezentujący typy ról użytkowników w systemie.
 * Używany do kontroli dostępu i uprawnień.
 */
public enum RoleType {
    /**
     * Rola administratora z pełnymi uprawnieniami.
     */
    ROLE_ADMIN,

    /**
     * Standardowa rola użytkownika z podstawowymi uprawnieniami.
     */
    ROLE_USER
}